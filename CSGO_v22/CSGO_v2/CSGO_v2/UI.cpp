void ui::BeginGroup(ImVec2 Size, const char* name, ...)
{
	ImGui::BeginChild(std::format("{}##{}", name, name).c_str(), Size, true, ImGuiWindowFlags_AlwaysAutoResize);
	ImGui::Text(name);
	ImGui::Separator();
}

void ui::EndGroup()
{
	ImGui::EndChild();
}

